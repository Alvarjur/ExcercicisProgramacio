# Dibuixar
def app_draw():
    screen.fill(WHITE)
    utils.draw_grid(pygame, screen, 50)

    rows = 15
    columns = 21

    hueStep = (360 / columns)

    for row in range(0, rows):
        for column in range(0, columns):
            hue = hueStep * column

            color = utils.hsl_to_rgb(hue, 1, (1/15) * row)
            pygame.draw.rect(screen, color, ((50 + column * 25, 50 + row * 25), (25, 25)))

    pygame.display.update()

if __name__ == "__main__":
    main()